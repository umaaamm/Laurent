                    <p>PEMBENTUKAN P2TP2A<br /> PERLUNYA WADAH UNTUK MEMFASILITASI PEMECAHAN MASALAH YANG DIALAMI OLEH PEREMPUAN/MASYARAKAT DI BIDANG:</p>
<ol>
<li>Pendidikan;</li>
<li>Kesehatan dan Lingkungan Hidup</li>
<li>Ekonomi dan Tenaga Kerja;</li>
<li>Hukum, Politik dan Pengambilan Keputusan</li>
<li>Informasi,</li>
<li>Perlindungan Tindak Kekerasan dan Perdagangan Orang</li>
</ol>
<p style="text-align: center;"><strong>Visi dan Misi</strong> : </p>
<p><strong>Visi</strong> :</p>
<p>Terwujudnya anak dan perempuan di Provinsi Lampung yang terhindar dari ancaman kejahatan dan tindak pidana lainnya demi menegakkan Hak Azasi Manusia sesuai dengan harkat dan martabatnya sebagai Warga Negara</p>
<p><strong>Misi</strong> :</p>
<p>Memberikan layanan secara mudah dan cepat kepada korban</p>
<ul>
<li>Menyelenggarakan perlindungan dan pemenuhan rehabilitasi kesehatan, pemulangan, reintegrasi sosial dan bantuan hukum</li>
<li>Melakukan jejaring dengan Rumah Sakit dan Dinas Sosial untuk penagnanan korban melalui rujukan</li>
<li>Melakukankerjasama lembaga pemerintah antar Provinsi dalam rangka Rehabilitasi Sosial pemulangan korban</li>
</ul>
<p style="text-align: center;"><br /> <strong>Peran P2TP2A</strong></p>
<ul>
<li>Wahana pelayanan pemberdayaan perempuan dan perlindungan anak yang berbasis masyarakat</li>
<li>pelayanan yang berperan dalam upaya pemenuhan kebutuhan peningkatan pendidikan, kesehatan, ekonomi, penanggulangan tindak kekerasan terhadap perempuan dan perlindungan anak serta peningkatan posisi dan kondisi perempuan dalam masyarakat</li>
<li>Upaya untuk mewujudkan kesetaraan dan keadilan gender antara laki-laki dan perempuan, mengingat kualitas hidup perempuan masih jauh tertinggal dari laki-laki hampir di seluruh bidang pembangunan terutama bidang pendidikan, kesehatan dan ekonomi.</li>
<li>Pusat data dan informasi, pusat rujukan dan memberikan berbagai jenis pelayanan baik fisik maupun psikologis.</li>
</ul>
<p style="text-align: center;"><strong>Dasar Pembentukan</strong> <br /> <br /> Instruksi Presiden No. 9 tahun 2000 tentang Pengarusutamaan Gender dalam Pembangunan Nasional;</p>
<ol>
<li>Konferensi Dunia tentang Perempuan ke-5 Tahun 2000 di New York;</li>
<li>UU No. 23 tahun 2002 tentang Perlindungan Anak;</li>
<li>UU No. 12 Tahun 2003 tentang Pemilu yang mengamanatkan kepada Partai Politik peserta Pemilu untuk dapat mencalonkan anggota DPR-RI, DPRD Propinsi dan DPRD Kabupaten/ Kota dengan memperhatikan Keterwakilan Perempuan sekurang-kurangnya 30%;</li>
</ol>
<p>UU No. 23 tahun 2004 tentang Penghapusan Kekerasan dalam Rumah Tangga;</p>
<p style="text-align: center;"><strong>Pendampingan Kasus:</strong></p>
<p><strong><em>Secara Hukum</em></strong><em>:</em></p>
<ol>
<li>Di Kepolisian pendampingan pada saat pelaporan, pembuatan Berita Acara Pemeriksaan (BAP), memberikan masukan pasal-pasal yang digunakan, lobby dan monitoring perkembangan kasus.</li>
<li>Di Kejaksaan melakukan lobby dalam penyusunan dakwaan, tuntutan dan monitoring perkembangan kasus.</li>
<li>Di Pengadilan pendampingan pada saat persidangan, membuat surat dukungan untuk tuntutan dan putusan, Monitoring perkembangan kasus, Mengusulkan JPU agar menerima atau banding terhadap hasil putusan.</li>
</ol>
<p>&nbsp;</p>
<p><strong><em>Secara Non Hukum</em></strong>:</p>
<ol>
<li>Bantuan dan pendampingan medis untuk pengobatan luka dan <em>visum et repertum</em></li>
<li>Bantuan psikososial dan spiritual dengan konseling rutin untuk mengatasi dampak trauma&agrave; kembali percaya diri</li>
<li>Rujukan ke <em>shelter/</em>rumah aman&agrave; kerjasama dengan Dinas Sosial Lampung</li>
<li>Pelayanan sosial dan akses keterampilan&agrave; pendidikan life skill</li>
<li>Menguatkan korban untuk menyelesaikan kasusnya melalui jalur hukum dan memberikan pendampingan</li>
<li>Pemberian bantuan modal dalam rangka penguatan ekonomi korban pasca kasus, kerjasama dengan&nbsp; Dinsos Provinsi Lampung</li>
</ol>
